/**
 * op_add - adds two numbers given as a parameter
 * @a: number
 * @b: number
 * Return: the result of the operation
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - substracts two numbers given as a parameter
* @a: number
* @b: number
* Return: the result of the operation
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiply two numbers given as a parameter
* @a: number
* @b: number
* Return: the result of the operation
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - divide two numbers given as a parameter
* @a: number
* @b: number
* Return: the result of the operation
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - calculates de module of two numbers given as a parameter
* @a: number
* @b: number
* Return: the result of the operation
*/
int op_mod(int a, int b)
{
	return (a % b);
}
